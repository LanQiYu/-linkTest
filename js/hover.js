$(document).ready(function(){
    $('#a').hover(function() {
        $("#b").css('display', 'block');
    }, function() {
       $("#b").css('display', 'none');
    });

    $("#b").hover(function() {
       $(this).css('display', 'block');;
    }, function() {
       $(this).css('display', 'none');
    });
})

